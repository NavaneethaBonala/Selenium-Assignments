package loopingControls;

public class IfStmt {
	
	public static void main(String[] args) {
		
		
		// print 1 to 10
		// if found 3 print ajay, but dnt print 3
		
		for(int i=1; i<=10; i++) {
			
			if(i!=3 )
			{
				System.out.println(i);
			}else {
				System.out.println("Ajay");
			}
		}
		
		
		
		
	}

}
