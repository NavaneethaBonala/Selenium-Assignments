package loopingControls;

public class ForLoop {
	public static void main(String[] args) {
		//print name 10 times
		for (int i=1; i<=10; i++) {
			System.out.println("Nita");
		}
		
		 // print 10 to 1
		for (int i=10; i>=1; i--)
		{
			System.out.println(i);
		}
		
		// print -10 to 1
		for (int i=-10; i<=1; i--)
		{
			System.out.println(i);
		}
		
		// print 1 to 10

		for (int i=1; i<=10; i++)
		{
			System.out.println(i);
		}
	}

}
