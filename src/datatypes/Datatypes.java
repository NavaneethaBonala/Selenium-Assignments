package datatypes;

public class Datatypes {

	public static void main(String[] args) {

		/* 
		 * Primitive 
		 * -> numbers
		 * -> characters
		 * -> decimal 
		 * -> true/false
		 * 
		 * Non - primitive
		 * -> string , arrays, list 
		 */
		/*
		 * byte, short, int , long
		 */
		byte b = 21;
		System.out.println(b);
		
		short sh = 128;
		System.out.println(sh);
		
		int i = 25635;
		System.out.println(i);
		
		

		/*
		 * char, string
		 * 
		
		 */
		
		char str = 'N';
		System.out.println(str);
		
		/*
		 * float (6-7 decimals), double(15 - 16 decimals)
		 */
		
		float f = 10.5f;
		System.out.println(f);
		
		double d = 102556.255;
		System.out.println(d);
		
		/* 
		 * boolean = true, false
		 */
		boolean b1 = true;
		System.out.println(b1);

		boolean b2 = false;
		System.out.println(b2);
		
		
		//sequence of characters is string
		
		String  str1 = "Nita";
		System.out.println(str1);

	 // Wrapper class
	}

}
